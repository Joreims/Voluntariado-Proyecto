    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed
        // TODO add your handling code here:
        String usuario = TfUser.getText();
        String pass = TfPassword.getText();
        if (usuario.isEmpty() || pass.isEmpty()){
            JOptionPane.showMessageDialog(null, "Algun campo se encuentra vacio");
        }else {
            if (usuario.equals("UCA") && pass.equals("1234")){
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema de registros de voluntarios");
                FrmUsuario Adm = new FrmUsuario();
                Adm.setVisible(true);
                dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Su usuario o contraseÃ±a son incorrectos");
            }
        }
    }//GEN-LAST:event_BtnIngresarActionPerformed

